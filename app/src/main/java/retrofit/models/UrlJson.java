package retrofit.models;

public class UrlJson {
    private String full;
    private String small;

    public UrlJson() {
    }

    public String getFull() {
        return full;
    }

    public void setFull(String full) {
        this.full = full;
    }

    public String getSmall() {
        return small;
    }

    public void setSmall(String small) {
        this.small = small;
    }
}
